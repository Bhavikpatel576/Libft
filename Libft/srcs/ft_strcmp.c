#include "libft.h"

int		ft_strcmp(const char *s1, const char *s2)
{
	int i;
	int total;

	i = 0;
	total = 0;
	while (s1[i] != '\0')
	{
		total = total + (s1[i] - s2[i]);
		i++;
	}
	return (total);
}
