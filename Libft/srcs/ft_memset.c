#include "libft.h"

void				*ft_memset(void *s, int c, size_t n)
{
	int				i;
	unsigned char	*b;

	b = s;
	i = 0;
	if (!*b)
	{
		return (NULL);
	}
	while (n--)
	{
		b[i] = c;
		i++;
	}
	return (s);
}
